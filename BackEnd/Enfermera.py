class Enfermera():
    nombre = ""
    apellido = ""
    birthday = ""
    sexo = ""
    usuario_name = ""
    contrasena = ""
    telefono = ""

    def __init__(self , nombre, apellido, birthday, sexo, usuario_name, contrasena, telefono):
        self.nombre = nombre
        self.apellido = apellido
        self.birthday = birthday
        self.sexo = sexo
        self.usuario_name = usuario_name
        self.contrasena = contrasena
        self.telefono = telefono